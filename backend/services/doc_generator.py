def generate_documentation(github_url):
    """
    Main function to generate documentation from GitHub URL
    """
    try:
        # Step 1: Convert GitHub URL to DeepWiki URL
        deepwiki_url = github_to_deepwiki(github_url)
        print(f"Converted to DeepWiki URL: {deepwiki_url}")

        # Extract repo info for folder structure
        pattern = r'github\.com/([^/]+)/([^/]+)'
        match = re.search(pattern, github_url)
        owner = match.group(1)
        repo = match.group(2).replace('.git', '')

        # Create output directory: output/{owner}_{repo}
        repo_folder = f"{owner}_{repo}"
        output_dir = os.path.join('output', repo_folder)
        os.makedirs(output_dir, exist_ok=True)

        # Step 2: Run deepwiki_to_md scraper
        success = run_deepwiki_scraper(deepwiki_url, output_dir)

        if success:
            # The scraper creates files in: output/{repo_folder}/{repo}/md/*.md
            # We need to look for the md folder
            md_folder = None
            for root, dirs, files in os.walk(output_dir):
                if 'md' in dirs:
                    md_folder = os.path.join(root, 'md')
                    break

            if not md_folder:
                # Try direct path
                md_folder = os.path.join(output_dir, repo, 'md')

            print(f"Looking for markdown files in: {md_folder}")

            # Read all generated markdown files
            markdown_files = []
            if os.path.exists(md_folder):
                for file in os.listdir(md_folder):
                    if file.endswith('.md'):
                        filepath = os.path.join(md_folder, file)
                        with open(filepath, 'r', encoding='utf-8') as f:
                            content = f.read()
                            markdown_files.append({
                                'filename': file,
                                'content': content
                            })
            else:
                print(f"Warning: md folder not found at {md_folder}")

            # Combine all markdown for preview
            combined_markdown = f"# Documentation for {repo}\n\n"
            combined_markdown += f"**Generated from:** {deepwiki_url}\n\n"
            combined_markdown += f"**Total files:** {len(markdown_files)}\n\n"
            for md_file in markdown_files:
                combined_markdown += f"\n---\n## {md_file['filename']}\n\n{md_file['content']}\n\n"

            print(f"Generated {len(markdown_files)} markdown files")

            return {
                'markdown': combined_markdown,
                'markdown_files': markdown_files,
                'deepwiki_url': deepwiki_url,
                'github_url': github_url,
                'repo_folder': repo_folder,
                'md_folder_path': md_folder
            }
        else:
            raise Exception("Scraper failed to generate documentation")

    except Exception as e:
        raise Exception(f"Documentation generation failed: {str(e)}")
