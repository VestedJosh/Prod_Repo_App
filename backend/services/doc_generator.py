import subprocess
import os
import re
from datetime import datetime
import sys

def github_to_deepwiki(github_url):
    """
    Convert GitHub URL to DeepWiki URL
    Example: https://github.com/openai/openai-python -> https://deepwiki.com/openai/openai-python
    """
    # Extract owner and repo from GitHub URL
    pattern = r'github\.com/([^/]+)/([^/]+)'
    match = re.search(pattern, github_url)

    if not match:
        raise ValueError("Invalid GitHub URL format")

    owner = match.group(1)
    repo = match.group(2).replace('.git', '')

    deepwiki_url = f"https://deepwiki.com/{owner}/{repo}"
    return deepwiki_url

def run_deepwiki_scraper(deepwiki_url, output_dir):
    """
    Run the deepwiki_to_md scraper using the package from Desktop
    Returns the output directory where markdown files are saved
    """
    try:
        # Path to the deepwiki_to_md package (in Docker container or local)
        backend_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        deepwiki_path = os.path.join(backend_dir, 'deepwiki_to_md')

        # Fallback to absolute path if relative doesn't exist
        if not os.path.exists(deepwiki_path):
            deepwiki_path = '/app/deepwiki_to_md'

        # Get absolute path for output directory
        abs_output_dir = os.path.join(backend_dir, output_dir)

        # Run the scraper using Python module
        cmd = [
            sys.executable, '-m', 'deepwiki_to_md.run_scraper',
            deepwiki_url,
            '--output-dir', abs_output_dir
        ]

        print(f"Running command: {' '.join(cmd)}")
        print(f"Working directory: {deepwiki_path}")
        print(f"Output directory: {abs_output_dir}")

        # Run the scraper
        result = subprocess.run(
            cmd,
            cwd=deepwiki_path,
            capture_output=True,
            text=True,
            timeout=120  # 2 minute timeout
        )

        print(f"Return code: {result.returncode}")
        print(f"STDOUT: {result.stdout}")
        print(f"STDERR: {result.stderr}")

        if result.returncode == 0:
            print(f"Scraper completed successfully")
            return True
        else:
            print(f"Scraper failed with return code {result.returncode}")
            return False

    except subprocess.TimeoutExpired:
        print("Scraper timed out after 2 minutes")
        return False
    except Exception as e:
        print(f"Error running scraper: {str(e)}")
        import traceback
        traceback.print_exc()
        return False

